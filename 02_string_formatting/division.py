# remainder
integer_division = 13 // 5
print(integer_division)

remainder = 13 % 5
print(remainder)

# remainder of an odd number divided by two is always 1
# remainder of an even number divided by two is always 0

x = 37
remainder = x % 2
print(remainder)

# "//" or Floor division always results in an integer. Anything after the decimal point is thrown away.